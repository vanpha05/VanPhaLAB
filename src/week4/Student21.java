package week4;

/**
 *
 * @author LENOVO
 */
public class Student21 {

    private String id;
    private String name;
    private String semester;
    private String course;

    public Student21(String id, String name, String semester, String course) {
        this.id = id;
        this.name = name;
        this.semester = semester;
        this.course = course;
    }

    public Student21() {
    }
    

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSemester() {
        return semester;
    }

    public void setSemester(String semester) {
        this.semester = semester;
    }

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }
    
    

    @Override
    public String toString() {
        return id + " | " + name + " | " + semester + " | " + course;
    }

}
